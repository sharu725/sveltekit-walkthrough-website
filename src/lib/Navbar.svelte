<script>
  import { applyAction, enhance } from "$app/forms";
  import { invalidateAll } from "$app/navigation";
  import { page } from "$app/stores";
  import { onMount } from "svelte";
  import Moon from "./icons/moon.svelte";
  import Sun from "./icons/sun.svelte";

  const navs = [
    {
      title: "Home",
      href: "/",
    },
    {
      title: "About",
      href: "/about",
    },
    {
      title: "Services",
      href: "/services",
    },
    {
      title: "Contact",
      href: "/contact",
    },
    {
      title: "Blog",
      href: "/blog",
    },
  ];

  let currentTheme = "";

  onMount(() => {
    // currentTheme = document.documentElement.dataset.theme;
    const userPrefersDarkMode = window.matchMedia(
      "(prefers-color-scheme: dark)"
    ).matches;

    const hasUserSetDarkModeManually =
      document.documentElement.dataset.theme == "dark";

    if (!hasUserSetDarkModeManually) {
      setTheme(userPrefersDarkMode ? "dark" : "light");
    }
  });

  const setTheme = (theme) => {
    document.documentElement.dataset.theme = theme;
    document.cookie = `siteTheme=${theme};max-age=31536000;path="/"`;
    currentTheme = theme;
  };

  $: url = $page.url.href;
  $: routeId = $page.url.pathname;
</script>

<nav>
  <div class="container">
    <a class="logo" href="/">
      <h1>Start Bootstrap</h1>
    </a>
    <ul>
      {#each navs as { title, href }}
        <li>
          <a
            {href}
            class:active={href === "/" ? routeId === "/" : url.includes(href)}
            {title}>{title}</a
          >
        </li>
      {/each}

      <li class="relative">
        <form method="GET" data-sveltekit-reload>
          <input type="hidden" name="theme" value="dark" />
          <button class="moon" type="submit">
            <Moon />
          </button>
        </form>

        <form method="GET" data-sveltekit-reload>
          <input type="hidden" name="theme" value="light" />
          <button class="sun" type="submit">
            <Sun />
          </button>
        </form>
      </li>
    </ul>
  </div>
</nav>

